# take nothing return nothing
def hello():
    print("hello world")
hello()

# take nothing return something
def greet():
    return "hello world"
greet()

# take something return something
def add(x, y):
    return x + y
add(1, 2)

# take something return nothing
def print_two(a, b):
    print(a)
    print(b)
print_two(1, 2)